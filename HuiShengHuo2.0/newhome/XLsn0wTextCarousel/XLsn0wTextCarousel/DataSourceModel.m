#import "DataSourceModel.h"

@implementation DataSourceModel

+ (instancetype)dataSourceModelWithType:(NSString *)type title:(NSString *)title time:(NSString *)time URLString:(NSString *)URLString {
    DataSourceModel *model = [[DataSourceModel alloc] init];
    model.type = type;
    model.title = title;
    model.time = time;
    model.URLString = URLString;
    return model;
}

@end
